#include <stdio.h>
#include <stdlib.h>

int main()
{
 printf("hello world!\n");
 printf("%d \n",32);/// integer value
 printf("%ld \n",1231263213);/// large integer value
 printf("%f \n",32.2);/// float value
 printf("%lf \n",3222232324.211232131);/// large float value
 printf("%c \n",'a');///character  value
 printf("%s \n","hello world!");/// string value
 printf("%x \n",16); ///hexadecimal value
 return 0;
}
